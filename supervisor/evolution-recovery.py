# Recovery Orchestrator
# =============================================================================

class RecoveryOrchestrator:
    """
    Main orchestrator that ties together state mapping, design space querying,
    template splicing, and cascaded evaluation.
    """

    def __init__(
        self,
        config_path: Optional[str] = None,
        design_space_path: Optional[str] = None,
        templates_dir: Optional[str] = None,
    ):
        # Load configuration
        self.config = self._load_config(config_path or str(DEFAULT_CONFIG_PATH))

        # Override paths if provided
        ds_path = design_space_path or self.config.design_space_yaml or str(DEFAULT_DESIGN_SPACE)
        tmpl_dir = templates_dir or self.config.templates_dir or str(DEFAULT_TEMPLATES_DIR)
        workspace = self.config.workspace or str(SCRIPT_DIR / "recovery-workspace")
        self.reports_dir = Path(
            self.config.reports_dir or str(SCRIPT_DIR / "recovery-reports")
        )
        self.reports_dir.mkdir(parents=True, exist_ok=True)

        # Initialize components
        self.design_space = DesignSpace(ds_path)
        self.splicer = TemplateSplicer(tmpl_dir, workspace)
        self.evaluator = L0Evaluator()

    def _load_config(self, path: str) -> RecoveryConfig:
        """Load configuration from YAML."""
        p = Path(path)
        if not p.exists():
            logger.warning(f"Config not found at {path}, using defaults")
            return RecoveryConfig()

        with open(p) as f:
            raw = yaml.safe_load(f)

        recovery = raw.get("recovery", {})
        return RecoveryConfig(
            min_stuck_cycles=recovery.get("min_stuck_cycles", 3),
            max_candidates_per_run=recovery.get("max_candidates_per_run", 6),
            eval_levels=recovery.get("eval_levels", ["L0", "L1"]),
            full_eval_threshold=recovery.get("full_eval_threshold", 5),
            auto_deploy=recovery.get("auto_deploy", False),
            cooldown_cycles=recovery.get("cooldown_cycles", 10),
            current_config=recovery.get("current_config", {}),
            tried_alternatives=recovery.get("tried_alternatives", []),
            design_space_yaml=recovery.get("design_space_yaml", ""),
            templates_dir=recovery.get("templates_dir", ""),
            workspace=recovery.get("workspace", ""),
            reports_dir=recovery.get("reports_dir", ""),
        )

    def run(
        self,
        state: str,
        stuck_cycles: int,
        current_config: Optional[dict[str, str]] = None,
        seed_kernel: Optional[str] = None,
        dry_run: bool = False,
    ) -> dict[str, Any]:
        """
        Run evolution-guided recovery.

        Args:
            state: Supervisor state (e.g., C0_NO_WORKER, DISPATCHING)
            stuck_cycles: Number of consecutive cycles in this state
            current_config: Current CULink config (overrides config file)
            seed_kernel: Path to the seed kernel file
            dry_run: If True, just show what would happen

        Returns:
            JSON-serializable report dict
        """
        timestamp = time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime())
        run_id = f"recovery_{state}_{int(time.time())}"

        # Step 1: Map state to dimension
        dimension, reason = STATE_TO_DIMENSION.get(state, (None, f"Unknown state: {state}"))

        report: dict[str, Any] = {
            "run_id": run_id,
            "timestamp": timestamp,
            "state": state,
            "stuck_cycles": stuck_cycles,
            "dimension_targeted": dimension,
            "reason": reason,
            "candidates_tried": 0,
            "candidates": [],
            "best_candidate": None,
            "recommendation": "",
            "dry_run": dry_run,
        }

        if dimension is None:
            report["recommendation"] = f"No kernel recovery possible: {reason}"
            logger.info(f"State {state}: {reason}")
            self._save_report(report)
            return report

        # Step 2: Check stuck cycle threshold
        if stuck_cycles < self.config.min_stuck_cycles:
            report["recommendation"] = (
                f"Stuck for {stuck_cycles} cycles, threshold is "
                f"{self.config.min_stuck_cycles}. Waiting."
            )
            logger.info(report["recommendation"])
            self._save_report(report)
            return report

        # Step 3: Get current config
        config = current_config or self.config.current_config
        if not config:
            config = {
                "backend": "d2h_ring_fi_send",
                "placement": "split_put_wait",
                "sync": "d2h_ring",
                "issuer": "multi_warp",
                "granularity": "per_expert_batch",
            }
        report["current_config"] = config
        current_value = config.get(dimension, "")

        # Step 4: Enumerate alternatives
        alternatives = self.design_space.get_alternatives(dimension, current_value)
        logger.info(
            f"Dimension '{dimension}': current='{current_value}', "
            f"alternatives={alternatives}"
        )

        # Step 5: Filter by constraints
        valid_alternatives = []
        for alt in alternatives:
            violations = self.design_space.check_constraints(config, dimension, alt)
            if violations:
                logger.info(f"  {alt}: SKIPPED (violates {violations})")
            else:
                # Also skip already-tried alternatives
                tried_key = f"{dimension}={alt}"
                if tried_key in self.config.tried_alternatives:
                    logger.info(f"  {alt}: SKIPPED (already tried)")
                else:
                    valid_alternatives.append(alt)
                    logger.info(f"  {alt}: VALID")

        # Limit candidates
        valid_alternatives = valid_alternatives[: self.config.max_candidates_per_run]
        report["valid_alternatives"] = valid_alternatives

        if not valid_alternatives:
            report["recommendation"] = (
                f"No valid alternatives for {dimension} (current={current_value}). "
                f"All alternatives either violate constraints or were already tried."
            )
            self._save_report(report)
            return report

        if dry_run:
            report["recommendation"] = (
                f"DRY RUN: Would try {len(valid_alternatives)} alternatives for "
                f"{dimension}: {valid_alternatives}"
            )
            for alt in valid_alternatives:
                info = self.design_space.get_value_info(dimension, alt)
                desc = info.get("description", "No description")
                if isinstance(desc, str):
                    desc = desc.strip()[:120]
                report["candidates"].append({
                    "alternative": alt,
                    "description": desc,
                    "status": "would_try",
                })
            report["candidates_tried"] = 0
            self._save_report(report)
            return report

        # Step 6: Generate and evaluate candidates
        if seed_kernel is None:
            # Default seed: transport_worker.cpp
            seed_kernel = str(
                Path("<HOME>/<project>"
                     "DeepEP/csrc/bolt/transport_worker.cpp")
            )
            if not Path(seed_kernel).exists():
                # Fallback to baseline header
                seed_kernel = str(
                    CUCO_DIR / "kernels" / "baseline" / "internode_header.cu"
                )

        if not Path(seed_kernel).exists():
            report["recommendation"] = f"Seed kernel not found: {seed_kernel}"
            self._save_report(report)
            return report

        candidates: list[RecoveryCandidate] = []
        for alt in valid_alternatives:
            cid = f"{run_id}_{dimension}_{alt}"
            candidate = RecoveryCandidate(
                id=cid,
                dimension=dimension,
                original_value=current_value,
                alternative_value=alt,
                template_path=None,
                candidate_path=None,
            )

            # Splice template
            candidate_path = self.splicer.splice(
                seed_kernel, dimension, alt, cid
            )
            if candidate_path is None:
                candidate.eval_level = "no_template"
                candidate.eval_diagnostics = (
                    f"No template available for {dimension}={alt}"
                )
                candidates.append(candidate)
                continue

            candidate.candidate_path = candidate_path
            candidate.template_path = str(
                self.splicer.available_templates(dimension).get(alt, "")
            )

            # L0 evaluation
            passed, diag = self.evaluator.evaluate(candidate_path)
            candidate.eval_level = "L0_parse"
            candidate.eval_passed = passed
            candidate.eval_diagnostics = diag

            if passed:
                candidate.eval_score = 0.1  # Passed L0
                # Try L1 if configured
                if "L1" in self.config.eval_levels:
                    l1_passed, l1_diag = self._try_l1_compile(candidate_path)
                    if l1_passed:
                        candidate.eval_level = "L1_compile"
                        candidate.eval_score = 0.5
                        candidate.eval_diagnostics = l1_diag
                    else:
                        candidate.eval_level = "L1_compile_failed"
                        candidate.eval_score = 0.1
                        candidate.eval_errors = l1_diag
            else:
                candidate.eval_score = 0.0
                candidate.eval_errors = diag

            candidates.append(candidate)
            logger.info(
                f"  Candidate {alt}: {candidate.eval_level} "
                f"passed={candidate.eval_passed} score={candidate.eval_score}"
            )

        # Step 7: Rank candidates
        candidates.sort(key=lambda c: c.eval_score, reverse=True)
        report["candidates"] = [c.to_dict() for c in candidates]
        report["candidates_tried"] = len(candidates)

        # Step 8: Select best
        best = candidates[0] if candidates and candidates[0].eval_score > 0 else None
        if best:
            report["best_candidate"] = best.to_dict()
            report["recommendation"] = (
                f"Best recovery candidate: {dimension}={best.alternative_value} "
                f"(eval_level={best.eval_level}, score={best.eval_score}). "
            )
            if self.config.auto_deploy and best.eval_score >= 0.5:
                report["recommendation"] += "AUTO-DEPLOY: enabled, deploying candidate."
            elif best.eval_score >= 0.5:
                report["recommendation"] += (
                    "Candidate passed L1 compile. Set auto_deploy=true in config "
                    "to enable automatic deployment, or deploy manually."
                )
            else:
                report["recommendation"] += (
                    "Candidate only passed L0. Manual review recommended before deployment."
                )
        else:
            report["recommendation"] = (
                f"No viable candidates found for {dimension} recovery. "
                f"All alternatives failed evaluation."
            )

        self._save_report(report)
        return report

    def _try_l1_compile(self, candidate_path: str) -> tuple[bool, str]:
        """
        Attempt L1 compile check. This is a lightweight check that verifies
        the file can be parsed by a C++ compiler (syntax check only).
        Falls back gracefully if nvcc/g++ is not available.
        """
        import subprocess

        # Try g++ syntax check first (faster, more available)
        for compiler in ["g++", "nvcc"]:
            try:
                if compiler == "g++":
                    cmd = [
                        "g++", "-fsyntax-only", "-std=c++17",
                        "-x", "c++",
                        "-I/opt/amazon/efa/include",
                        candidate_path,
                    ]
                else:
                    cmd = [
                        "nvcc", "-c", "--dry-run",
                        "-arch=sm_90a",
                        "-I/opt/amazon/efa/include",
                        candidate_path,
                    ]

                proc = subprocess.run(
                    cmd,
                    capture_output=True,
                    text=True,
                    timeout=30,
                )
                if proc.returncode == 0:
                    return True, f"L1 compile OK ({compiler})"
                else:
                    # Extract first few error lines
                    err_lines = [
                        line for line in proc.stderr.splitlines()
                        if "error" in line.lower()
                    ][:5]
                    return False, f"L1 compile failed ({compiler}): " + "; ".join(err_lines)

            except FileNotFoundError:
                continue
            except subprocess.TimeoutExpired:
                return False, f"L1 compile timed out ({compiler})"
            except Exception as e:
                continue

        return False, "No compiler available (tried g++, nvcc)"

