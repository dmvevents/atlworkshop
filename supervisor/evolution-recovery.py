@dataclass
class RecoveryCandidate:
    """A recovery candidate generated by template splicing."""
    id: str
    dimension: str
    original_value: str
    alternative_value: str
    template_path: Optional[str]
    candidate_path: Optional[str]
    eval_level: str = "pending"
    eval_passed: bool = False
    eval_score: float = 0.0
    eval_errors: Optional[str] = None
    eval_diagnostics: Optional[str] = None

    def to_dict(self) -> dict:
        return asdict(self)


@dataclass
class RecoveryConfig:
    """Parsed recovery configuration."""
    min_stuck_cycles: int = 3
    max_candidates_per_run: int = 6
    eval_levels: list[str] = field(default_factory=lambda: ["L0", "L1"])
    full_eval_threshold: int = 5
    auto_deploy: bool = False
    cooldown_cycles: int = 10
    current_config: dict[str, str] = field(default_factory=dict)
    tried_alternatives: list[str] = field(default_factory=list)
    design_space_yaml: str = ""
    templates_dir: str = ""
    workspace: str = ""
    reports_dir: str = ""


# =============================================================================
# Design Space Loader
# =============================================================================

class DesignSpace:
    """Loads and queries the CULink-EFA design space YAML."""

    def __init__(self, yaml_path: str):
        self.yaml_path = yaml_path
        with open(yaml_path) as f:
            self.raw = yaml.safe_load(f)

        self.dimensions = {}
        for dim_name in ["backend", "placement", "sync", "issuer", "granularity"]:
            if dim_name in self.raw:
                self.dimensions[dim_name] = self.raw[dim_name]

        self.constraints = self.raw.get("constraints", [])

    def get_values(self, dimension: str) -> list[str]:
        """Return all possible values for a dimension."""
        dim = self.dimensions.get(dimension, {})
        return list(dim.get("values", {}).keys())

    def get_default(self, dimension: str) -> str:
        """Return the default value for a dimension."""
        dim = self.dimensions.get(dimension, {})
        return dim.get("default", "")

    def get_value_info(self, dimension: str, value: str) -> dict:
        """Return metadata for a specific dimension value."""
        dim = self.dimensions.get(dimension, {})
        return dim.get("values", {}).get(value, {})

    def get_alternatives(self, dimension: str, current_value: str) -> list[str]:
        """Return all values for a dimension except the current one."""
        all_values = self.get_values(dimension)
        return [v for v in all_values if v != current_value]

    def check_constraints(
        self, config: dict[str, str], target_dim: str, new_value: str
    ) -> list[str]:
        """
        Check if setting target_dim=new_value violates any hard constraints.
        Returns a list of violated constraint names (empty = OK).
        """
        test_config = dict(config)
        test_config[target_dim] = new_value

        violations = []
        for constraint in self.constraints:
            if constraint.get("severity") != "hard":
                continue
            rule = constraint.get("rule", "")
            name = constraint.get("name", "unnamed")
            if self._rule_violated(rule, test_config):
                violations.append(name)

        return violations

    def _rule_violated(self, rule: str, config: dict[str, str]) -> bool:
        """
        Simple rule evaluator for constraint checking.
        Handles patterns like:
          "B=gdrcopy_mmio OR S=gdrcopy_mmio => gdrdrv kernel module loaded"
          "G=bulk_all => P must be sequential"
          "I=cpu_worker => S != d2h_ring"
          "B=staging_copy_bulk AND G in {per_token, adaptive_chunk} => INVALID"
        """
        B = config.get("backend", "")
        P = config.get("placement", "")
        S = config.get("sync", "")
        I = config.get("issuer", "")
        G = config.get("granularity", "")

        # bulk_all_requires_sequential
        if "G=bulk_all" in rule and "P must be sequential" in rule:
            if G == "bulk_all" and P != "sequential":
                return True

        # cpu_worker_no_d2h_ring
        if "I=cpu_worker" in rule and "S != d2h_ring" in rule:
            if I == "cpu_worker" and S == "d2h_ring":
                return True

        # fully_overlapped_needs_fine_sync
        if "P=fully_overlapped" in rule and "S in {" in rule:
            if P == "fully_overlapped":
                allowed = {"host_pinned_volatile", "d2h_ring", "gdrcopy_mmio"}
                if S not in allowed:
                    return True

        # staging_copy_conflicts_with_fine_granularity
        if "B=staging_copy_bulk" in rule and "G in {per_token, adaptive_chunk}" in rule:
            if B == "staging_copy_bulk" and G in ("per_token", "adaptive_chunk"):
                return True

        # fi_write_requires_dmabuf — soft check (we note it but don't block)
        # We only block hard structural incompatibilities above.

        return False


# =============================================================================
# Template Splicer
# =============================================================================

class TemplateSplicer:
    """Generates recovery candidates by splicing templates into a seed kernel."""

    def __init__(self, templates_dir: str, workspace: str):
        self.templates_dir = Path(templates_dir)
        self.workspace = Path(workspace)
        self.workspace.mkdir(parents=True, exist_ok=True)

    def available_templates(self, dimension: str) -> dict[str, Path]:
        """Return {value_name: template_path} for a dimension."""
        result = {}
        pattern = f"{dimension}_*.cpp.tmpl"
        for tmpl_path in sorted(self.templates_dir.glob(pattern)):
            # Extract value name: backend_fi_write.cpp.tmpl -> fi_write
            stem = tmpl_path.stem.replace(".cpp", "")  # backend_fi_write
            value_name = stem.replace(f"{dimension}_", "", 1)  # fi_write
            result[value_name] = tmpl_path
        return result

    def _match_template(
        self, templates: dict[str, Path], alternative_value: str
    ) -> Optional[Path]:
        """
        Find the best matching template for an alternative value.

        Matching strategy (in order):
          1. Exact match: template name == alternative value
          2. Prefix match: alternative starts with template name
          3. Reverse prefix: template name starts with alternative
          4. Normalized match: strip common prefixes (d2h_ring_, host_pinned_)
             and compare
          5. Substring match: template name is contained in alternative or
             vice versa
        """
        # 1. Exact match
        if alternative_value in templates:
            return templates[alternative_value]

        # 2-5. Fuzzy matching
        strip_prefixes = ["d2h_ring_", "host_pinned_", "staging_copy_"]
        def normalize(s: str) -> str:
            result = s
            for pfx in strip_prefixes:
                result = result.replace(pfx, "")
            return result

        alt_norm = normalize(alternative_value)

        for tmpl_name, tmpl_path in templates.items():
            # 2. Prefix match
            if alternative_value.startswith(tmpl_name):
                return tmpl_path
            # 3. Reverse prefix
            if tmpl_name.startswith(alternative_value):
                return tmpl_path
            # 4. Normalized match
            tmpl_norm = normalize(tmpl_name)
            if alt_norm == tmpl_norm:
                return tmpl_path
            # 5. Substring match
            if tmpl_norm in alt_norm or alt_norm in tmpl_norm:
                return tmpl_path

        return None

    def splice(
        self,
        seed_path: str,
        dimension: str,
        alternative_value: str,
        candidate_id: str,
    ) -> Optional[str]:
        """
        Create a recovery candidate by splicing a template into the seed.

        Returns the path to the generated candidate file, or None if no
        template is available.
        """
        templates = self.available_templates(dimension)

        template_path = self._match_template(templates, alternative_value)

        if template_path is None:
            logger.warning(
                f"No template found for {dimension}={alternative_value} "
                f"(available: {list(templates.keys())})"
            )
            return None

        # Read seed and template
        seed_content = Path(seed_path).read_text()
        template_content = template_path.read_text()

        # Strategy: append the template as a clearly-marked alternative
        # section at the end of the seed file, wrapped in #ifdef guards.
        # This ensures the file is syntactically valid C++ for L0/L1 eval.
        guard_name = f"RECOVERY_{dimension.upper()}_{alternative_value.upper()}"
        guard_name = re.sub(r"[^A-Z0-9_]", "_", guard_name)

        spliced = seed_content + f"""

// =============================================================================
// RECOVERY CANDIDATE: {dimension}={alternative_value}
// Generated by evolution-recovery.py
// Template: {template_path.name}
// =============================================================================
#ifdef {guard_name}
{template_content}
#endif // {guard_name}
"""

        # Write candidate
        candidate_dir = self.workspace / candidate_id
        candidate_dir.mkdir(parents=True, exist_ok=True)
        candidate_path = candidate_dir / f"bolt_worker_{alternative_value}.cpp"
        candidate_path.write_text(spliced)

        return str(candidate_path)


# =============================================================================
# L0 Parse Evaluator (standalone, no CUCo dependency)
# =============================================================================

class L0Evaluator:
    """
    Minimal L0 parse check that does not depend on the CUCo evaluation module.
    Checks structural validity of C++ source files.
    """

    @staticmethod
    def evaluate(candidate_path: str) -> tuple[bool, str]:
        """
        Run L0 parse check on a candidate file.
        Returns (passed, diagnostics).
        """
        path = Path(candidate_path)
        if not path.exists():
            return False, f"File not found: {candidate_path}"

        content = path.read_text()
        errors = []

        # Check for balanced braces
        brace_depth = 0
        for i, ch in enumerate(content):
            if ch == '{':
                brace_depth += 1
            elif ch == '}':
                brace_depth -= 1
                if brace_depth < 0:
                    errors.append(f"Unmatched closing brace at char {i}")
                    break
        if brace_depth > 0:
            errors.append(f"Unmatched opening braces: {brace_depth} unclosed")

        # Check for #include
        if "#include" not in content:
            errors.append("No #include directives found")

        # Check template markers are balanced
        starts = content.count("TEMPLATE-MARKER:")
        ends = content.count("TEMPLATE-MARKER-END:")
        if starts > 0 and starts != ends:
            errors.append(f"Unbalanced template markers: {starts} starts, {ends} ends")

        if errors:
            return False, "; ".join(errors)
        return True, "L0 parse OK"


