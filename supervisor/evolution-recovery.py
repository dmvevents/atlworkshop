def parse_current_config(config_str: str) -> dict[str, str]:
    """
    Parse a current config string like 'B=x,P=y,S=z,I=w,G=v'
    into a dimension dict.
    """
    result = {}
    for part in config_str.split(","):
        part = part.strip()
        if "=" not in part:
            continue
        key, val = part.split("=", 1)
        key = key.strip()
        val = val.strip()
        # Map short symbols to full names
        dim_name = DIM_SYMBOL_MAP.get(key, key)
        result[dim_name] = val
    return result


