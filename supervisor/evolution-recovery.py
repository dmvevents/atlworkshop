#!/usr/bin/env python3
"""
Evolution-Guided Recovery for the Bolt Supervisor

When the Transport supervisor detects a persistent stuck state (same failure
for N consecutive cycles), this script:

  1. Maps the stuck state to the most-likely-failing CULink-EFA dimension
  2. Loads the design space YAML and enumerates alternatives for that dimension
  3. Filters out alternatives that violate constraints
  4. For each valid alternative, generates a recovery candidate by splicing
     a code template into the seed kernel
  5. Runs cascaded evaluation (L0 parse + L1 compile at minimum)
  6. Ranks candidates and outputs a JSON report

Can be triggered manually or auto-triggered by bolt-supervisor-v2.sh.

Usage:
    # Manual: investigate C0_NO_WORKER stuck state
    python3 evolution-recovery.py --state C0_NO_WORKER --stuck-cycles 5 \\
        --seed-kernel /path/to/transport_worker.cpp

    # Dry-run: show what would happen without evolving
    python3 evolution-recovery.py --state DISPATCHING --stuck-cycles 3 --dry-run

    # Demo mode: simulated run with mock data
    python3 evolution-recovery.py --demo
"""

